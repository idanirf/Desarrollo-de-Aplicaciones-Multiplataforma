/**
 * Funcion que nos devulve el perimetro de un cuadrado.
 * @param numero numero que queremos averiguar si es positivo o negativo.
 * @returns devolvemos el valor.
 */
 function esPositivo(numero: number) {
    if(numero>=0){
        console.log("El numero es positivo")
    }else{
        if(numero<=0){
           console.log("El numero es negativo")
        }
    }
 
    return esPositivo;
}

